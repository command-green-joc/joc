import Stripe from 'stripe';
const stripe = new Stripe(process.env.STRIPE_SECRET);

export default async function handler(req, res) {
  const sig = req.headers['stripe-signature'];
  let event;
  try { event = stripe.webhooks.constructEvent(req.body, sig, process.env.STRIPE_WEBHOOK_SECRET); }
  catch (err) { return res.status(400).send(`Webhook Error: ${err.message}`); }

  if (event.type === 'checkout.session.completed') {
    const session = event.data.object;
    const { type, query, dates } = session.metadata;

    let report;
    if (type === 'vault') {
      report = await generateVaultReport(query);
    } else {
      report = await generateCaseReport(query, dates);
    }

    await sendEmail(session.customer_details.email, report);
    await uploadToArweave(report);
  }

  res.json({ received: true });
}
